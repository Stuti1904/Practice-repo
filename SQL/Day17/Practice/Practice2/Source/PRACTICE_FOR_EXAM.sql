--SCALAR FUNCTION
CREATE FUNCTION MEALNAME (@MEALID INT)
RETURNS  VARCHAR(100)
AS
BEGIN
DECLARE @MEALNAME VARCHAR(100)
SET @MEALNAME= (SELECT MEALNAME FROM MEAL WHERE MEALID=@MEALID)
RETURN @MEALNAME
END

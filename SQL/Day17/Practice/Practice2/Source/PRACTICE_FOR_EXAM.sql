CREATE VIEW HUMANRESOURCES.HIREDATE
AS
SELECT P.FIRSTNAME, P.LASTNAME, E.HIREDATE
FROM HumanResources.Employee AS E JOIN Person.Person AS P
ON E.BusinessEntityID=P.BusinessEntityID

